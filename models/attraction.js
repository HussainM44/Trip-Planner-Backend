// models/Attraction.js

const mongoose = require("mongoose")

const attractionSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    city: {
      type: String,
      required: true,
      trim: true,
    },
    country: {
      type: String,
      required: true,
      trim: true,
    },
    picture: {
      // âœ… matches your seeder data
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
  },
  { timestamps: true }
)

const Attraction = mongoose.model("Attraction", attractionSchema)

module.exports = Attraction
